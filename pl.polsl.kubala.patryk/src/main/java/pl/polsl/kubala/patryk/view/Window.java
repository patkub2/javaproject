    public int getChoice()
    {
        int choiceNumber =0;
        
       
        do
        {
            if( encryptRadio.isSelected()){
            choiceNumber = 0;
        }else
        if( decryptRadio.isSelected()){
            choiceNumber = 1;
        }
        }
        while(choiceNumber<0||choiceNumber>1);
        return choiceNumber;
    }
